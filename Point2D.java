public class Point2D
{
    //Fields
    private int x;
    private int y;

    //Mutator methods (setters)
    public void setX(int inputX)
    {
        x = inputX;
    }

     public void setY(int inputY)
    {
        y = inputY;
    }

    //Accessors (getters)
    public int getX()
    {
        return x;
    }

     public int getY()
    {
        return y;
    }
}
